package org.octocode.booking.parser.laterooms.dto;

public class RoomFacility {
    private String facility_name;
    private String facility_group_name;

    public String getFacility_name() {
        return facility_name;
    }

    public void setFacility_name(String facility_name) {
        this.facility_name = facility_name;
    }

    public String getFacility_group_name() {
        return facility_group_name;
    }

    public void setFacility_group_name(String facility_group_name) {
        this.facility_group_name = facility_group_name;
    }
}
